package ВводДанных_Условия;


import java.util.Scanner;





/**
 * Константы final
 */
public class Constans {
    public static void main(String[] args) {
        // ключевое слово final означает, что переменная является константой
        // и ее значение можно задать только один раз и менять его больше нельзя
        final Scanner sc = new Scanner(System.in);
        final double PI = 3.14; // постоянное число
    }
}
