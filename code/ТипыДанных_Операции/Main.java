package ТипыДанных_Операции;


/**
 * Функция main, функции print и println, комментарии в языке Java
 */
public class Main {
    public static void main(String[] args) {
        // Функция print выводит текст без переноса курсора на следующую строку
        System.out.print("Hello world!"); // без переноса строки
        // Функция printlf после вывода текста так же переносит курсор на следующую строку
        System.out.println("Hello!"); // с переносом строки после вывода сообщения

        // выводить можно как числа, так и строки
        System.out.println(10);

        // это односрочные комментарии, нужно писать два слеша // в каждой строке
        // dajsdsakjndsajknadsjk
        // osdjoasd
        // oasdjohads

        /* многострочные комментарии позволяют не писать каждый раз два слеша //
        asduhaiusdhids
        adsoihasdiuhiasd
        adsohasduohasd
        oaisdhoiuasdhou

        aodisoiasdhoiahdso
         */
    }
}